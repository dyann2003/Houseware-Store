    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        CartDAO cartdao = new CartDAO();
        ProductDAO pdao = new ProductDAO();
        User u = (User) session.getAttribute("account");

        // Phan cart
        Cart cart = null;
        Object o = session.getAttribute("cart");
        // Check
        if (o != null) {
            cart = (Cart) o;
        } else {
            cart = new Cart();
        }
        String tRid = request.getParameter("rid");
        int rid;
        try {
            if (u != null) {
                rid = Integer.parseInt(tRid);
                Product p = pdao.getProductByID(rid);
//            cart.removeItem(rid);
                cartdao.deleteItemCart(p, u);
            } else {
                request.getRequestDispatcher("home");
            }

        } catch (Exception e) {
        }
        double total = cartdao.getTotalCart(u);
        List<Item> list = cartdao.getCartByUserName(u.getUserName());
        session.setAttribute("cart", cart);
        session.setAttribute("total", total);
        session.setAttribute("listItemsInCart", list);
        session.setAttribute("cartSize", list.size());

        //
        request.getRequestDispatcher("viewcart.jsp").forward(request, response);

    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

